
	ORG _DATA
OSNAME	STR "ZHUOS"

	ORG _TEXT
RESET	CLD
	LDA #$04 ; init basic line low
	STA BASH
	LDA #!0
	CMP LOC0
	BEQ INIT
	JMP RESET
INIT	LDA #$5A
	STA (BASL),Y
	INY
	LDA #$48
	STA (BASL),Y
	INY
	LDA #$55
	STA (BASL),Y
	INY
	LDA #$4F
	STA (BASL),Y
	INY
	LDA #$53
	STA (BASL),Y
	INC LOC0
	LDA #$80
	STA BASL

	JMP RESET



	ORG _TEXT
IRQENTRY	NOP
	JSR KEYIN
	NOP
	RTI
